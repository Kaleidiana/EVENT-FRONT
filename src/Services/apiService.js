import axios from 'axios';


const API_URL = 'http://localhost:5000/api';

// Events API
export const getEvents = () => axios.get(`${API_URL}/events`);
export const getEventById = (id) => axios.get(`${API_URL}/events/${id}`);
export const createEvent = (event) => axios.post(`${API_URL}/events`, event);
export const updateEvent = (id, event) => axios.put(`${API_URL}/events/${id}`, event);
export const deleteEvent = (id) => axios.delete(`${API_URL}/events/${id}`);

// Users API
export const getUsers = () => axios.get(`${API_URL}/users`);
export const getUserById = (id) => axios.get(`${API_URL}/users/${id}`);
export const createUser = (user) => axios.post(`${API_URL}/users`, user);
export const updateUser = (id, user) => axios.put(`${API_URL}/users/${id}`, user);
export const deleteUser = (id) => axios.delete(`${API_URL}/users/${id}`);
